/**
 * Класс Calculator предоставляет базовые арифметические операции.
 */
class Calculator {
    
    /**
     * Складывает два целых числа.
     * @param a первое целое число
     * @param b второе целое число
     * @return сумма a и b
     */
    public int add(int a, int b) {
        return a + b;
    }
}
