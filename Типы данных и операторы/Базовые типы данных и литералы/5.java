public class Main {
    public static void main(String[] args) {
        int a = 5;
        double b = 2.5;
        double result = a + b;
        System.out.println(result); // Вывод: 7.5
    }
} //При сложении int и double результатом будет double. int автоматически преобразуется в double для выполнения операции.
